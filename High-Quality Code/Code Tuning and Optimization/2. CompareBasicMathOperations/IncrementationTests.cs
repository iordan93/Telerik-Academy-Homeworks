namespace _2.CompareBasicMathOperations
{
    using System;

    internal class IncrementationTests
    {
        internal static void IntIncrementationTest(int start, int repetitions)
        {
            for (int i = 0; i < repetitions; i++)
            {
                start++;
            }
        }

        internal static void LongIncrementationTest(long start, int repetitions)
        {
            for (int i = 0; i < repetitions; i++)
            {
                start++;
            }
        }

        internal static void FloatIncrementationTest(float start, int repetitions)
        {
            for (int i = 0; i < repetitions; i++)
            {
                start++;
            }
        }

        internal static void DoubleIncrementationTest(double start, int repetitions)
        {
            for (int i = 0; i < repetitions; i++)
            {
                start++;
            }
        }

        internal static void DecimalIncrementationTest(decimal start, int repetitions)
        {
            for (int i = 0; i < repetitions; i++)
            {
                start++;
            }
        }
    }
}
